connect(subsystem, "Speed Integrator/1", "Speed Squared/1");
connect(subsystem, "Speed Squared/1", "Aero Gain/1");
connect(subsystem, "Traction Gain/1", "Net Force Sum/1");
connect(subsystem, "Brake Gain/1", "Net Force Sum/2");
connect(subsystem, "Aero Gain/1", "Net Force Sum/3");
connect(subsystem, "Rolling Resistance/1", "Net Force Sum/4");
