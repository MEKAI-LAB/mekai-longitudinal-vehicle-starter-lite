function build_longitudinal_vehicle_starter()
% Build the MEKAI longitudinal vehicle starter model.

setup_longitudinal_vehicle_starter();

model = "mekai_longitudinal_vehicle_starter";
modelFile = model + ".slx";

if bdIsLoaded(model)
    close_system(model, 0);
end

if isfile(modelFile)
    delete(modelFile);
end

new_system(model);
load_system("simulink");

set_param(model, ...
    "StopTime", "simConfig.stopTime", ...
    "Solver", "ode45", ...
    "SaveFormat", "StructureWithTime", ...
    "ReturnWorkspaceOutputs", "on");

add_block("simulink/Sources/From Workspace", model + "/Target Speed", ...
    "VariableName", "targetSpeedProfile", ...
    "Position", [60 90 180 120]);

add_block("simulink/Sources/From Workspace", model + "/Road Grade", ...
    "VariableName", "roadGradeProfile", ...
    "Position", [60 220 180 250]);

add_block("simulink/Ports & Subsystems/Subsystem", model + "/Driver Controller", ...
    "Position", [260 50 520 190]);
add_block("simulink/Ports & Subsystems/Subsystem", model + "/Vehicle Plant", ...
    "Position", [610 50 930 260]);

buildDriverController(model + "/Driver Controller");
buildVehiclePlant(model + "/Vehicle Plant");

add_block("simulink/Sinks/To Workspace", model + "/Vehicle Speed Out", ...
    "VariableName", "vehicleSpeed_mps", ...
    "SaveFormat", "Array", ...
    "Position", [980 80 1090 110]);
add_block("simulink/Sinks/To Workspace", model + "/Vehicle Accel Out", ...
    "VariableName", "vehicleAccel_mps2", ...
    "SaveFormat", "Array", ...
    "Position", [980 125 1090 155]);
add_block("simulink/Sinks/To Workspace", model + "/Distance Out", ...
    "VariableName", "vehicleDistance_m", ...
    "SaveFormat", "Array", ...
    "Position", [980 170 1090 200]);
add_block("simulink/Sinks/To Workspace", model + "/Net Force Out", ...
    "VariableName", "netForce_N", ...
    "SaveFormat", "Array", ...
    "Position", [980 215 1090 245]);
add_block("simulink/Sinks/To Workspace", model + "/Throttle Out", ...
    "VariableName", "throttleCmd", ...
    "SaveFormat", "Array", ...
    "Position", [560 85 650 115]);
add_block("simulink/Sinks/To Workspace", model + "/Brake Out", ...
    "VariableName", "brakeCmd", ...
    "SaveFormat", "Array", ...
    "Position", [560 135 650 165]);
add_block("simulink/Sinks/To Workspace", model + "/Target Speed Out", ...
    "VariableName", "targetSpeedRef_mps", ...
    "SaveFormat", "Array", ...
    "Position", [260 255 380 285]);
add_block("simulink/Sinks/To Workspace", model + "/Road Grade Out", ...
    "VariableName", "roadGrade_deg", ...
    "SaveFormat", "Array", ...
    "Position", [260 305 380 335]);

connect(model, "Target Speed/1", "Driver Controller/1");
connect(model, "Road Grade/1", "Vehicle Plant/3");
connect(model, "Vehicle Plant/1", "Driver Controller/2");
connect(model, "Driver Controller/1", "Vehicle Plant/1");
connect(model, "Driver Controller/2", "Vehicle Plant/2");
connect(model, "Vehicle Plant/1", "Vehicle Speed Out/1");
connect(model, "Vehicle Plant/2", "Vehicle Accel Out/1");
connect(model, "Vehicle Plant/3", "Distance Out/1");
connect(model, "Vehicle Plant/4", "Net Force Out/1");
connect(model, "Driver Controller/1", "Throttle Out/1");
connect(model, "Driver Controller/2", "Brake Out/1");
connect(model, "Target Speed/1", "Target Speed Out/1");
connect(model, "Road Grade/1", "Road Grade Out/1");

save_system(model, modelFile);
close_system(model);
end

function buildDriverController(subsystem)
