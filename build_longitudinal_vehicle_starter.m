add_block("simulink/Ports & Subsystems/In1", subsystem + "/TargetSpeed", ...
    "Position", [30 48 60 62]);
add_block("simulink/Ports & Subsystems/In1", subsystem + "/ActualSpeed", ...
    "Position", [30 108 60 122]);
add_block("simulink/Math Operations/Sum", subsystem + "/Speed Error", ...
    "Inputs", "+-", ...
    "Position", [95 72 120 108]);
add_block("simulink/Math Operations/Gain", subsystem + "/Kp", ...
    "Gain", "driver.Kp", ...
    "Position", [155 55 225 85]);
add_block("simulink/Continuous/Integrator", subsystem + "/Integrator", ...
    "InitialCondition", "0", ...
    "Position", [160 112 190 142]);
add_block("simulink/Math Operations/Gain", subsystem + "/Ki", ...
    "Gain", "driver.Ki", ...
    "Position", [225 112 295 142]);
add_block("simulink/Math Operations/Sum", subsystem + "/Command Sum", ...
    "Inputs", "++", ...
    "Position", [330 74 355 116]);
add_block("simulink/Discontinuities/Saturation", subsystem + "/Throttle Saturation", ...
    "LowerLimit", "0", ...
    "UpperLimit", "1", ...
    "Position", [395 50 435 80]);
add_block("simulink/Math Operations/Gain", subsystem + "/Negative Command", ...
    "Gain", "-1", ...
    "Position", [392 112 432 142]);
add_block("simulink/Discontinuities/Saturation", subsystem + "/Brake Saturation", ...
    "LowerLimit", "0", ...
    "UpperLimit", "1", ...
    "Position", [465 112 505 142]);
add_block("simulink/Ports & Subsystems/Out1", subsystem + "/ThrottleCmd", ...
    "Position", [550 55 580 69]);
add_block("simulink/Ports & Subsystems/Out1", subsystem + "/BrakeCmd", ...
    "Position", [550 117 580 131]);

connect(subsystem, "TargetSpeed/1", "Speed Error/1");
connect(subsystem, "ActualSpeed/1", "Speed Error/2");
connect(subsystem, "Speed Error/1", "Kp/1");
connect(subsystem, "Speed Error/1", "Integrator/1");
