function connect(systemName, source, destination)
add_line(systemName, source, destination, "autorouting", "on");
end

function delete_contents(systemName)
blocks = find_system(systemName, "SearchDepth", 1, "Type", "Block");
blocks = string(blocks);
blocks = blocks(blocks ~= systemName);

for block = blocks'
    delete_block(block);
end
end

function delete_line_if_exists(systemName)
lines = find_system(systemName, "FindAll", "on", "Type", "line");
for idx = 1:numel(lines)
    delete_line(lines(idx));
end
end
