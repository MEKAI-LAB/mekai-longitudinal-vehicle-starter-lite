connect(subsystem, "Kp/1", "Command Sum/1");
connect(subsystem, "Ki/1", "Command Sum/2");
connect(subsystem, "Command Sum/1", "Throttle Saturation/1");
connect(subsystem, "Command Sum/1", "Negative Command/1");
connect(subsystem, "Negative Command/1", "Brake Saturation/1");
connect(subsystem, "Throttle Saturation/1", "ThrottleCmd/1");
connect(subsystem, "Brake Saturation/1", "BrakeCmd/1");
end

